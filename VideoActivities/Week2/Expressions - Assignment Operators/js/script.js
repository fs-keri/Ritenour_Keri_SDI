//alert("Testing to see if connected");

var a = 3;
a + 4;
console.log(a); //if you want to do more with and use a then you need to assign a

a = a+4;
console.log(a);

//add
a+=4; //this is the same as writing a = a+4
console.log(a);

//multiply
a *=4;
console.log(a);

//divide
a /=4;
console.log(a);

//subtract
a -=4;
console.log(a);

//increment operator
a ++; //adds 1
console.log(a);

a --; //subtracts 1
console.log(a);